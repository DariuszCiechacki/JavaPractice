package course.simpletypes;

public class J06Variables {

    public static void main(String[] args) {
        String initialText = "Hello World!";
        String addText = "It is Dariusz!";

        System.out.println(initialText + " " + addText);

        int initialNumber = 1;
        int addNumber = 9;

        int result = initialNumber + addNumber;
        System.out.println("result is: " + result);
    }
}
